        <h2>About Library And Information Science</h2>

        <P> Library Science or Librarianship has been defined as the systematic body of knowledge related to the book
            and the library, including diverse aspects dealing with books as autonomous entities, but also those related
            to the entity of which they are a part 1. On the other hand, it has also been pointed out that Information
            Science is concerned with the principles and practices of production, organization and distribution of
            information and the study of information from its generation to its use and transmission in a variety of
            ways 2. Likewise, that Library Science and Information Science are specific disciplines with their own study
            subject and specific activity, which together with others like Archival science and Bibliography integrate
            an interrelated field.</P>
        <P>There is also another point of view which views the study of recorded information as a general discipline,
            which agglutinates other disciplines, as branches or specialties. This proposal has been successful in the
            United States of America and and it also has had great influence in the Latin American Spanish-speaking
            countries, where Library and Information Science has been used as the name of a general discipline covering
            all study aspects of recorded information (in numerical, alphabetical and graphic symbols) 3. Nevertheless,
            the terminology used has motivated an intense debate among scholars and professionals in this region, and
            they have not yet reached a conclusion. For example, the name of library schools are different in diverse
            Latin American Spanish-speaking countries, and in some of them Librarianship, Library Science and Library
            and Information Science have been used synonymously.</P>
        <P>The above highlights the need for studies to achieve a closer approximation of the way in which Library and
            Information Science (LIS) is conceptualized and how it is used, including possible answers to the following
            questions : « Is LIS one or different disciplines ? » In either case, what are the differences or
            convergences ? What are the study fields ? What kind of study subjects or phenomena are approached ? The
            present work aims to achieve possible answers to these questions, as well as to identify, analyze and
            discuss how these terms are conceptualized and used at library schools in Latin American Spanish-speaking
            countries.</P>

